dia = int(input("Ingrese un número entre 1 y 7 "))

if dia == 1:
    print(f"El día correspondiente para el número {dia} es LUNES")
elif dia == 2:
    print(f"El día correspondiente para el número {dia} es MARTES")
elif dia == 3:
    print(f"El día correspondiente para el número {dia} es MIERCOLES")
elif dia == 4:
    print(f"El día correspondiente para el número {dia} es JUEVES")
elif dia == 5:
    print(f"El día correspondiente para el número {dia} es VIERNES")
elif dia == 6:
    print(f"El día correspondiente para el número {dia} es SÁBADO")
elif dia == 7:
    print(f"El día correspondiente para el número {dia} es DOMINGO")
else:
    print("Ingrese un número valido")
